SELECT 
    TO_CHAR(SYSDATE, 'DD-MM-YYYY HH24:MI:SS') AS current_date_time_1,
    TO_CHAR(CURRENT_DATE, 'DD-MM-YYYY HH24:MI:SS') AS current_date_time_2
