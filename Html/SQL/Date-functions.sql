--ADD MONTHS--

SYNTAX: SELECT ADD_MONTHS(DATE TYPE VALUE,NUMBER VALUE)
EX:
SELECT ADD_MONTHS('14-JAN-2020',9) FROM DUAL;

SELECT ADD_MONTHS('14-FEB-2020',10.84) FROM DUAL;

SELECT ADD_MONTHS('14-FEB-2020',-9) FROM DUAL;

